DROP TABLE POST;
CREATE TABLE POST
(
    ID   BIGINT GENERATED BY DEFAULT AS IDENTITY,
    NAME VARCHAR(255),
    TITLE VARCHAR(500),
    CONTENTS VARCHAR(2000),
    CREATED_DATE TIMESTAMP NOT NULL,
    MODIFIED_DATE TIMESTAMP NOT NULL,
    PRIMARY KEY (ID)
);

DROP TABLE COMMENT;
CREATE TABLE COMMENT
(
    ID   BIGINT GENERATED BY DEFAULT AS IDENTITY,
    POST_ID BIGINT NOT NULL,
    COMMENT VARCHAR(1000) NOT NULL,
    CREATED_DATE TIMESTAMP NOT NULL,
    MODIFIED_DATE TIMESTAMP NOT NULL,
    PRIMARY KEY (ID)
);

